class Shape
{
    public String name;
    public String color;
    public int x;
    public int y;
    
    public Shape(String name, String color, int x, int y) {
    	this.name = name;
    	this.color = color;
    	this.x = x;
    	this.y = y;
    }
    public Shape(String name, int x, int y) {
    	this.name = name;
    	this.x = x;
    	this.y = y;
    }
 };